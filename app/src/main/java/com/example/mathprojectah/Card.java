package com.example.mathprojectah;

import android.graphics.drawable.Drawable;


public class Card {
    private int name;
    private boolean bomb;
    private int drawable;

    public Card(){
        name = name;
    }

    public int getName(){return name;}

    public boolean getBomb(){return bomb;}

    public int getDrawable(){return drawable;}

    public void setName(int name){this.name=name;}

    public void setBomb(boolean bomb){this.bomb = bomb;}

    public void setDrawable(){this.drawable = drawable;}
}
